# Exercise: PatternMatching-1
# Which of the following will match?

# a = [1, 2, 3]
true  
# a = 4
true
# 4 = a
true
# [a, b] = [ 1, 2, 3 ]
false
# a = [ [ 1, 2, 3 ] ] 
true
# [a] = [ [ 1, 2, 3 ] ]
true
# [[a]] = [ [ 1, 2, 3 ] ]
false

